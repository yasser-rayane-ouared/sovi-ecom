def format_block(keys_dict):
    out = []
    for k, v in keys_dict.items():
        # Escape quotes inside value
        val_esc = v.replace('"', '\\"')
        out.append(f'    {k}: "{val_esc}",')
    return "\n".join(out) + "\n"

